<template>
  <li>
    <h3>{{ fullName }}</h3>
    <h4>${{ rate }}</h4>
    <div>
      <base-badge
        v-for="area in areas"
        :key="area"
        :type="area"
        :title="area"
      ></base-badge>
    </div>
    <div class="actions">
      <!-- :to will bind the to value to the computed property values -->
      <base-button mode="outline" link :to="coachContactLink"
        >Contact</base-button
      >
      <base-button link :to="coachDetails">View Details</base-button>
    </div>
  </li>
</template>
<script>
export default {
  props: ['id', 'firstName', 'lastName', 'rate', 'areas'],
  computed: {
    // since the template is interpolating 'fullName' this computed property will trigger and return the-
    // requested values via props
    fullName() {
      return this.firstName + ' ' + this.lastName;
    },
    coachContactLink() {
      // this.$route.path will dynamically insert the current path then build a longer path
      return this.$route.path + '/' + this.id + '/contact'; // coaches/c1/contact
    },
    coachDetails() {
      return '/coaches/' + this.id; // coaches/c1
    }
  }
};
</script>
